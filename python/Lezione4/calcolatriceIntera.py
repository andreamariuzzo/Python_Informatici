# permetti all'utente di inserire due numeri interi e calcola il risultato delle seguenti operazioni: +, -, *, /, **2

num1 = int(input("Scrivi un numero intero "))   
num2 = int(input("Scrivi un altro numero intero "))

somma = num1 + num2
differenza = num1 - num2
prodotto = num1 * num2
divisione = num1 / num2
potenza = num1 **2
potenza2 = num2 **2

print("la somma è:", somma)
print("la differenza è:", differenza)
print("il prodotto è:", prodotto)
print("la divisione è:", divisione)
print("la potenza è:", potenza)
print("la potenza è:", potenza2)
