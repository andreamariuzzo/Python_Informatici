# Modulo %, restituisce il resto della divisione

print(5%2) #5 / 2 = 2 resto 1

print(14 % 3) #14 / 3 = 4 resto 2

# Potenza **

print(4**6) #4^6 = 4096
print(3**2) #3^2 = 9

# Divisione intera //

print(5.0//2.0) #5 / 2 = 2.5, ma con // si arrotonda per difetto, quindi 2.0

