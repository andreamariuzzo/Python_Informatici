# esistono i seguente tipi di dati: String, Number (Integer, Floats), Boolean

print("Questo è un esempio di stringa")
print("2") #anche questa è una stringa
print(2) #questo è un integer
print(2.0) #questo è un float
print(True) #questo è un boolean
print(False) #questo è un boolean
print(1<2) #questo è un boolean

